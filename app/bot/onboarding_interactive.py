def get_task_details(task_text: str) -> Dict[str, Any]:
    """
    Возвращает подробные инструкции для задачи
    """
    task_lower = task_text.lower()
    
    if any(word in task_lower for word in ["доступ", "логин", "пароль", "почта", "crm", "1с"]):
        return TASK_DETAILS.get("доступы", {})
    elif any(word in task_lower for word in ["документ", "регламент", "политик", "изуч", "инструкц"]):
        return TASK_DETAILS.get("документация", {})
    elif any(word in task_lower for word in ["знаком", "команд", "коллег", "представ"]):
        return TASK_DETAILS.get("знакомство", {})
    elif any(word in task_lower for word in ["рабочее место", "техник", "ноутбук", "настрой", "компьютер", "оборуд"]):
        return TASK_DETAILS.get("рабочее_место", {})
    elif any(word in task_lower for word in ["процесс", "работа", "задача", "выполн"]):
        return TASK_DETAILS.get("процессы", {})
    elif any(word in task_lower for word in ["дресс", "одежд", "форма"]):
        return TASK_DETAILS.get("дресс_код", {})
    elif any(word in task_lower for word in ["первый день", "начало", "приветств"]):
        return TASK_DETAILS.get("онбординг_первый_день", {})
    elif any(word in task_lower for word in ["обратная связь", "фидбек", "оценка"]):
        return TASK_DETAILS.get("обратная_связь", {})
    else:
        return {}


def format_task_details(task_details: Dict[str, Any], task_text: str) -> str:
    """
    Форматирует подробные инструкции для вывода
    """
    if not task_details:
        return (
            f"❓ **Как выполнить задачу:**\n\n"
            f"📌 **Задача:** {task_text}\n\n"
            f"📖 **Рекомендации:**\n"
            f"• Внимательно прочитайте задание\n"
            f"• Уточните непонятные моменты у наставника\n"
            f"• Сделайте заметки для себя\n\n"
            f"💡 *Если нужна помощь — не стесняйтесь спрашивать!*"
        )
    
    output = []
    output.append(f"📌 **{task_details.get('title', task_text)}**")
    output.append("")
    
    if task_details.get('detailed_instructions'):
        output.append(task_details['detailed_instructions'])
        output.append("")
    
    if task_details.get('contact_person'):
        output.append(f"👤 **К кому обратиться:** {task_details['contact_person']}")
    
    if task_details.get('estimated_time'):
        output.append(f"⏱️ **Примерное время:** {task_details['estimated_time']}")
    
    if task_details.get('video_link'):
        output.append(f"📹 **Видеоинструкция:** {task_details['video_link']}")
    
    return "\n".join(output)


