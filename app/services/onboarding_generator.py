def detect_level_by_keywords(role_text: str) -> str:
    """Определяет уровень по ключевым словам в должности"""
    lower_text = role_text.lower()
    if "junior" in lower_text or "джуниор" in lower_text or "стажёр" in lower_text or "trainee" in lower_text or "помощник" in lower_text:
        return "junior"
    elif "senior" in lower_text or "сеньор" in lower_text or "lead" in lower_text or "principal" in lower_text or "ведущий" in lower_text or "главный" in lower_text:
        return "senior"
    elif "middle" in lower_text or "мидл" in lower_text:
        return "middle"
    return "middle"


