def generate_onboarding_plan(
    candidate: Dict[str, Any],
    department: str = "development",
    level: str = None,
    start_date: Optional[str] = None,
    max_tasks: int = 10,
    max_meetings: int = 5
) -> Dict[str, Any]:
    """Генерация плана онбординга для сотрудника (универсальная версия)"""
    
    if level is None:
        if department == "development":
            level = "middle"
        else:
            level = "default"
    
    dept_template = ONBOARDING_TEMPLATES.get(department, ONBOARDING_TEMPLATES["development"])
    
    if level in dept_template["levels"]:
        level_data = dept_template["levels"][level]
    else:
        level_data = dept_template["levels"]["default"]
    
    if start_date:
        start = datetime.strptime(start_date, "%Y-%m-%d").date()
    else:
        start = datetime.now(timezone.utc).date()
    
    candidate_id = candidate.get("id")
    candidate_name = candidate.get("name", "Новый сотрудник")
    candidate_email = candidate.get("email")
    candidate_exp = candidate.get("experience_years") or candidate.get("experience", 0)
    
    checklist = []
    tasks = level_data["tasks"][:max_tasks]
    for i, task in enumerate(tasks, 1):
        deadline = start + timedelta(days=i - 1)
        checklist.append({
            "id": i,
            "task": task,
            "status": "pending",
            "deadline": deadline.isoformat(),
            "deadline_readable": deadline.strftime("%d.%m.%Y"),
            "planned_day": i
        })
    
    meetings = []
    for meeting in level_data["meetings"][:max_meetings]:
        meeting_date = start + timedelta(days=meeting["day"] - 1)
        meetings.append({
            "date": meeting_date.isoformat(),
            "date_readable": meeting_date.strftime("%d.%m.%Y"),
            "time": meeting["time"],
            "with": meeting["with"],
            "topic": meeting["topic"],
            "status": "scheduled"
        })
    
    return {
        "candidate_id": candidate_id,
        "candidate_name": candidate_name,
        "candidate_email": candidate_email,
        "candidate_experience": candidate_exp,
        "department": dept_template["name"],
        "department_key": department,
        "level": level,
        "start_date": start.isoformat(),
        "start_date_readable": start.strftime("%d.%m.%Y"),
        "checklist": checklist,
        "meetings": meetings,
        "recommendations": level_data.get("recommendations", []),
        "created_at": datetime.now(timezone.utc).isoformat()
    }


def format_onboarding_plan(plan: Dict[str, Any]) -> str:
    """Красивый вывод плана онбординга (универсальный)"""
    output = []
    output.append("📋 **ПЛАН ОНБОРДИНГА**")
    output.append("")
    output.append(f"👤 Сотрудник: {plan['candidate_name']}")
    output.append(f"🏢 Отдел: {plan['department']}")
    if plan['department_key'] == 'development' and plan.get('level') and plan['level'] != 'default':
        output.append(f"📊 Уровень: {plan['level'].upper()}")
    if plan.get('candidate_experience'):
        output.append(f"💼 Опыт: {plan['candidate_experience']} лет")
    output.append(f"📅 Дата начала: {plan['start_date_readable']}")
    
    output.append("")
    output.append("**✅ ЧТО НУЖНО СДЕЛАТЬ**")
    for task in plan['checklist']:
        output.append(f"  • День {task['planned_day']} ({task['deadline_readable']}): {task['task']}")
    
    output.append("")
    output.append("**📅 С КЕМ И КОГДА ВСТРЕТИТЬСЯ**")
    for meeting in plan['meetings']:
        output.append(f"  • {meeting['date_readable']} {meeting['time']} — {meeting['with']}: {meeting['topic']}")
    
    if plan.get('recommendations'):
        output.append("")
        output.append("**💡 ПОЛЕЗНЫЕ СОВЕТЫ**")
        for rec in plan['recommendations']:
            output.append(f"  • {rec}")
    
    output.append("")
    output.append("💪 Удачи! Если будут вопросы — обращайся к наставнику или команде.")
    
    return "\n".join(output)
